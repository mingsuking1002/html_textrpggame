function defineSheet(key, title, headers, aliases = [], headerNotes = []) {
  return Object.freeze({
    key,
    title,
    aliases: Object.freeze([...aliases]),
    headers: Object.freeze([...headers]),
    headerNotes: Object.freeze(
      headers.map((header, index) => headerNotes[index] || `${header} 컬럼 설명이 아직 정의되지 않았습니다.`),
    ),
  });
}

